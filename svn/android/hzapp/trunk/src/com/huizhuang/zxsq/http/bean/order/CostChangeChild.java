package com.huizhuang.zxsq.http.bean.order;

public class CostChangeChild {

	private int cost_type;
	private String cost_name;
	private String cost_number;
	private String cost_price;
	private String total;
	private String cause;
	public int getCost_type() {
		return cost_type;
	}
	public void setCost_type(int cost_type) {
		this.cost_type = cost_type;
	}
	public String getCost_name() {
		return cost_name;
	}
	public void setCost_name(String cost_name) {
		this.cost_name = cost_name;
	}
	public String getCost_number() {
		return cost_number;
	}
	public void setCost_number(String cost_number) {
		this.cost_number = cost_number;
	}
	public String getCost_price() {
		return cost_price;
	}
	public void setCost_price(String cost_price) {
		this.cost_price = cost_price;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getCause() {
		return cause;
	}
	public void setCause(String cause) {
		this.cause = cause;
	}
	
	

}
